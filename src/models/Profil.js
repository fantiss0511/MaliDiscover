class Profil {
  constructor(
    id_profil,
    nom,
    email,
    mot_de_passe,
    role,
    date_creation,
    photo_profil,
    adresse,
    telephone,
    status
  ) {
    this.id_profil = id_profil;
    this.nom = nom;
    this.email = email;
    this.mot_de_passe = mot_de_passe;
    this.role = role;
    this.date_creation = date_creation;
    this.photo_profil = photo_profil;
    this.adresse = adresse;
    this.telephone = telephone;
    this.status = status;
  }
}
