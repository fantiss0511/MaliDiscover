class Activite {
    constructor(
        id_activite,
        nom,
        type_activite,
        lieu,
        contact,
        email,
        description,
        prix,
        payment,
        category,
        image
    ) {
        this.id_activite = id_activite;
        this.nom = nom;
        this.type_activite = type_activite;
        this.lieu = lieu;
        this.contact = contact;
        this.email = email;
        this.description = description;
        this.prix = prix;
        this.payment = payment;
        this.category = category;
        this.image = image;
    }
}
