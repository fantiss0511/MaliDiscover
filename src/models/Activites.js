class Activite {
    constructor(
        id_activite,
        nom,
        location,
        phone,
        email,
        description,
        prix,
        payment,
        category,
        image
    ) {
        this.id_activite = id_activite;
        this.nom = nom;
        this.location = location;
        this.phone = phone;
        this.email = email;
        this.description = description;
        this.prix = prix;
        this.payment = payment;
        this.category = category;
        this.image = image;
    }
}
